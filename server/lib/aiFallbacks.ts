import { createHash } from 'crypto';
import type {
  IdeaBankSuggestion,
  IdeaBankSuggestResponse,
  IdeaBankTemplateResponse,
  TemplateSlotSuggestion,
} from '@shared/types/ideaBank';

export interface FallbackCopyVariation {
  headline: string;
  hook: string;
  bodyText: string;
  cta: string;
  caption: string;
  hashtags: string[];
  framework: string;
  qualityScore: {
    clarity: number;
    persuasiveness: number;
    platformFit: number;
    brandAlignment: number;
    overallScore: number;
    reasoning: string;
  };
  characterCounts: {
    headline: number;
    hook: number;
    body: number;
    caption: number;
    total: number;
  };
}

function cleanText(value: string, fallback: string): string {
  const trimmed = value.trim();
  return trimmed.length > 0 ? trimmed : fallback;
}

function hashToIndex(seed: string, size: number): number {
  const hash = createHash('sha1').update(seed).digest('hex');
  const num = Number.parseInt(hash.slice(0, 8), 16);
  return num % Math.max(size, 1);
}

export function buildFallbackImageUrl(prompt: string, resolution: '1K' | '2K' | '4K' = '2K'): string {
  const sizeMap: Record<'1K' | '2K' | '4K', string> = {
    '1K': '1024x1024',
    '2K': '1536x1536',
    '4K': '2048x2048',
  };
  const shortPrompt = cleanText(prompt, 'Generated Preview').slice(0, 42);
  return `https://placehold.co/${sizeMap[resolution]}/png?text=${encodeURIComponent(shortPrompt)}`;
}

export function buildFallbackIdeaBankResponse(params: {
  userGoal?: string;
  maxSuggestions?: number;
  uploadDescriptions?: string[];
}): IdeaBankSuggestResponse {
  const goal = cleanText(params.userGoal ?? '', 'show practical product benefits on-site');
  const subject = cleanText(params.uploadDescriptions?.[0] ?? '', 'the selected product');
  const limit = Math.min(Math.max(params.maxSuggestions ?? 3, 1), 5);

  const templates = [
    `Wide-angle jobsite scene featuring ${subject} with clear workflow context`,
    `Close-up detail shot of ${subject} highlighting quality and finish`,
    `Before/after install sequence showing how ${subject} improves outcomes`,
    `Technician in-action composition demonstrating ${subject} in real use`,
    `Product + callout overlay design focused on reliability and speed`,
  ];

  const suggestions: IdeaBankSuggestion[] = Array.from({ length: limit }).map((_, i) => {
    const idx = (hashToIndex(`${goal}:${subject}`, templates.length) + i) % templates.length;
    const template = templates[idx] ?? templates[0] ?? 'Professional product campaign visual';
    const prompt = `${template}. Campaign goal: ${goal}. Keep the visual style clean, professional, and realistic.`;
    return {
      id: `fallback-idea-${i + 1}`,
      summary: `Fallback concept ${i + 1}: ${template.slice(0, 60)}`,
      prompt,
      mode: 'standard',
      reasoning: 'Generated by deterministic fallback because AI provider is temporarily unavailable.',
      confidence: Math.max(45, 70 - i * 4),
      sourcesUsed: {
        visionAnalysis: false,
        kbRetrieval: false,
        webSearch: false,
        templateMatching: false,
      },
      recommendedPlatform: 'linkedin',
      recommendedAspectRatio: '1:1',
    };
  });

  return {
    suggestions,
    analysisStatus: {
      visionComplete: false,
      kbQueried: false,
      templatesMatched: 0,
      webSearchUsed: false,
      productKnowledgeUsed: false,
      uploadDescriptionsUsed: params.uploadDescriptions?.length ?? 0,
      learnedPatternsUsed: 0,
    },
  };
}

