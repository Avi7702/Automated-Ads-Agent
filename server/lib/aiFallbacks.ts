export function buildFallbackCopyVariations(params: {
  productName: string;
  productDescription: string;
  platform: string;
  industry: string;
  count: number;
}): FallbackCopyVariation[] {
  const productName = cleanText(params.productName, 'Your product');
  const productDescription = cleanText(params.productDescription, 'Professional solution for demanding teams');
  const industry = cleanText(params.industry, 'your industry');
  const count = Math.min(Math.max(params.count, 1), 5);

  const hooks = [
    `Stop losing time on avoidable ${industry} delays.`,
    `Built for teams that need predictable results every week.`,
    `When deadlines are tight, reliability matters most.`,
    `Give your crew a solution that keeps projects moving.`,
    `A practical upgrade for modern field operations.`,
  ];

  return Array.from({ length: count }).map((_, i) => {
    const hookIdx = (hashToIndex(`${productName}:${productDescription}`, hooks.length) + i) % hooks.length;
    const hook = hooks[hookIdx] ?? hooks[0] ?? `Built for better ${industry} outcomes.`;
    const headline = `${productName} for Real-World Performance`.slice(0, 80);
    const bodyText = `${productDescription} Designed to help teams deliver cleaner installs, fewer callbacks, and better project consistency.`;
    const cta = 'Book a quick demo';
    const caption = `${hook} ${bodyText} ${cta}.`;
    const hashtags = ['#Construction', '#B2BMarketing', '#ProjectEfficiency'];
    const total = headline.length + hook.length + bodyText.length + caption.length;

    return {
      headline,
      hook,
      bodyText,
      cta,
      caption,
      hashtags,
      framework: 'FALLBACK',
      qualityScore: {
        clarity: 7,
        persuasiveness: 6,
        platformFit: 6,
        brandAlignment: 6,
        overallScore: 6,
        reasoning: 'Deterministic fallback copy used while AI provider is unavailable.',
      },
      characterCounts: {
        headline: headline.length,
        hook: hook.length,
        body: bodyText.length,
        caption: caption.length,
        total,
      },
    };
  });
}

export function buildFallbackAnalysisAnswer(params: {
  question: string;
  originalPrompt: string;
  editPrompt?: string | null;
}): string {
  const q = cleanText(params.question, 'How can I improve this output?');
  const original = cleanText(params.originalPrompt, 'the original prompt');
  const edit = cleanText(params.editPrompt ?? '', '');
  const editLine = edit ? `Last edit focus: "${edit}". ` : '';

  return `AI analysis is temporarily unavailable. ${editLine}Based on your prompt "${original}", review composition clarity, product visibility, and CTA readability first. For your question "${q}", prioritize stronger subject contrast, tighter framing, and one clear conversion action.`;
}

export function buildFallbackAgentReply(userMessage: string): string {
  const clean = cleanText(userMessage, 'your request').slice(0, 160);
  return `AI is temporarily unavailable, so I switched to deterministic guidance. For "${clean}", I recommend: 1) lock the primary campaign goal, 2) choose 1-3 products, 3) use a concrete scene prompt with audience + outcome, 4) run generation again once provider health recovers.`;
}
